def get_hardened_w3():
    # Priority list of reliable Polygon RPCs
    rpc_list = [
        os.getenv("RPC_URL"),
        "https://polygon-rpc.com",
        "https://rpc.ankr.com/polygon",
        "https://polygon.llamarpc.com",
        "https://1rpc.io/matic"
    ]
    
    print("üì° Initializing Network Handshake...")
    for url in rpc_list:
        if not url or len(url) < 10: continue
        try:
            # Clean URL and set strict timeout
            _w3 = Web3(Web3.HTTPProvider(url.strip(), request_kwargs={'timeout': 8}))
            if _w3.is_connected():
                _w3.middleware_onion.inject(ExtraDataToPOAMiddleware, layer=0)
                print(f"‚úÖ Linked to Cluster: {url[:25]}...")
                return _w3
        except Exception as e:
            print(f"‚ö†Ô∏è  Node Refused: {url[:20]}...")
            continue
    return None

