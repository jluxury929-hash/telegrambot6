async def handle_interaction(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    if query.data.startswith("SET_"):
        context.user_data['stake'] = int(query.data.split("_")[1])
        await query.edit_message_text(f"‚úÖ Stake set to ${context.user_data['stake']} Native USDC")
    elif query.data.startswith("PAIR_"):
        context.user_data['pair'] = query.data.split("_")[1]
        kb = [[InlineKeyboardButton("UP üìà", callback_data="EXEC_UP"), InlineKeyboardButton("DOWN üìâ", callback_data="EXEC_DOWN")]]
        await query.edit_message_text(f"üíé CLOB Target: **{context.user_data['pair']}**", reply_markup=InlineKeyboardMarkup(kb))
    elif query.data.startswith("EXEC_"):
        await run_api_execution(context, query.message.chat_id, "UP" if "UP" in query.data else "DOWN")

