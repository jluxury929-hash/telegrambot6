def get_user_vault(user_id):
    master = os.getenv("MASTER_KEY", os.getenv("WALLET_SEED", "HYDRA_PROTOCOL_2026"))
    seed = hashlib.sha256(f"{master}:{user_id}".encode()).hexdigest()
