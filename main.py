async def handle_callback(update, context):
    query = update.callback_query; await query.answer()
    if "SET_" in query.data:
        val = int(query.data.split("_")[1]); context.user_data['stake'] = val
        await query.edit_message_text(f"‚úÖ <b>LOAD: ${val}</b>", parse_mode='HTML')
    elif "HIT_" in query.data:
        idx = int(query.data.split("_")[1]); target = context.user_data['paths'][idx]
        stake = float(context.user_data.get('stake', 10))
        await query.edit_message_text(f"üöÄ <b>STRIKING:</b> <code>{target['name']}</code>", parse_mode='HTML')
        try:
            # Place Order with numeric Asset Token ID and Type 1 Signature
            order = await asyncio.to_thread(clob_client.create_market_order, MarketOrderArgs(
                token_id=target['token_id'], amount=stake, side=BUY
            ))
            resp = await asyncio.to_thread(clob_client.post_order, order, OrderType.FOK)
            msg = "‚úÖ SUCCESS" if resp.get("success") else f"‚ùå FAIL: {resp.get('errorMsg')}"
            await context.bot.send_message(query.message.chat_id, msg, parse_mode='HTML')
        except Exception as e:
            await context.bot.send_message(query.message.chat_id, f"‚ò¢Ô∏è ERROR: {str(e)[:100]}", parse_mode='HTML')

