    v = get_vault(q.from_user.id)
    if "SET_" in q.data:
        val = int(q.data.split("_")[1]); context.user_data['stake'] = val
        await q.edit_message_text(f"‚úÖ <b>STRIKE: ${val}</b>")
    elif q.data == "CONVERT_NATIVE":
        # (Uniswap logic preserved)
        pass 
    elif "INT_" in q.data:
        idx = int(q.data.split("_")[1]); target = OMNI_STRIKE_CACHE[idx]
        kb = [[InlineKeyboardButton("üí• EXECUTE", callback_data=f"EXE_{idx}")]]
        await q.edit_message_text(f"<b>TARGET:</b> {target['q']}", reply_markup=InlineKeyboardMarkup(kb), parse_mode='HTML')
    elif "EXE_" in q.data:
        idx = int(q.data.split("_")[1]); target = OMNI_STRIKE_CACHE[idx]
        stake = float(context.user_data.get('stake', 10))
        try:
            client = init_clob(v)
            args = MarketOrderArgs(token_id=str(target['token_id']), amount=stake, side=BUY, price=0.999)
            setattr(args, 'size', stake); setattr(args, 'expiration', 0)
            signed = await asyncio.to_thread(client.create_order, args)
            resp = await asyncio.to_thread(client.post_order, signed, OrderType.FOK)
            if resp.get("success"):
                await context.bot.send_message(q.message.chat_id, "üöÄ <b>STRIKE DEPLOYED!</b>")
                STRIKE_LOG[str(target['token_id'])] = {"q": target['q'], "stake": stake, "price": target['price'], "chat_id": q.message.chat_id}
            else: await context.bot.send_message(q.message.chat_id, f"‚ùå Failed: {resp.get('errorMsg')}")
        except Exception as e: await context.bot.send_message(q.message.chat_id, f"‚ö†Ô∏è Error: {str(e)}")

