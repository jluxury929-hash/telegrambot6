async def handle_interaction(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    if query.data.startswith("SET_"):
        context.user_data['stake'] = int(query.data.split("_")[1])
        await query.edit_message_text(f"‚úÖ Stake set to ${context.user_data['stake']}")
    elif query.data.startswith("PAIR_"):
        context.user_data['pair'] = query.data.split("_")[1]
        kb = [[InlineKeyboardButton("CALL üìà", callback_data="EXEC_UP"), InlineKeyboardButton("PUT üìâ", callback_data="EXEC_DOWN")]]
        await query.edit_message_text(f"üíé Pool: **{context.user_data['pair']}**", reply_markup=InlineKeyboardMarkup(kb))
    elif query.data.startswith("EXEC_"):
        await run_atomic_execution(context, query.message.chat_id, "UP" if "UP" in query.data else "DOWN")

async def autopilot_loop(chat_id, context):
    while auto_mode_enabled:
        target = random.choice(list(POOLS.keys()))
        await run_atomic_execution(context, chat_id, random.choice(["UP", "DOWN"]), asset_override=target)
        await asyncio.sleep(random.randint(60, 120))

